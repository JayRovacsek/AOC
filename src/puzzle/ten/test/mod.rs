#[test]
fn test_best_locations_asteroid_map() {
    assert_eq!(33, most_visible_point(generate_point_map("......#.#.#..#.#......#######..#.#.###...#..#.......#....#.##..#....#..##.#..#####...#..#..#....####", 10)));
    assert_eq!(35, most_visible_point(generate_point_map("#.#...#.#..###....#..#....#...##.#.#.#.#....#.#.#..##..###.#..#...##....##....##......#....####.###.", 10)));
    assert_eq!(41, most_visible_point(generate_point_map(".#..#..#######.###.#....###.#...###.##.###.##.#.#.....###..#..#.#..#.##..#.#.###.##...##.#.....#.#..", 10)));
    assert_eq!(210, most_visible_point(generate_point_map(".#..##.###...#########.############..##..#.######.########.#.###.#######.####.#.#####.##.#.##.###.##..#####..#.##############################.####....###.#.#.####.######################.##.###..####....######..##.###########.##.####...##..#.#####..#.######.#####...#.##########...#.##########.#######.####.#.###.###.#.##....##.##.###..#####.#.#.###########.####.#.#.#####.####.######.##.####.##.#..##", 20)));
}
